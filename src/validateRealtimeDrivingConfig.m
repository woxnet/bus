function config = validateRealtimeDrivingConfig(config)
%VALIDATEREALTIMEDRIVINGCONFIG Validate bounded causal monitor settings.

if ~isstruct(config) || ~isscalar(config)
    error('IMU:InvalidRealtimeConfig', 'Realtime config must be a scalar struct.');
end
required = fieldnames(getDefaultsWithoutValidation());
missing = setdiff(required, fieldnames(config));
if ~isempty(missing)
    error('IMU:InvalidRealtimeConfig', 'Missing realtime setting: %s.', missing{1});
end
positive = {'sampleRateHz','callbackPeriodMs','pollPeriodSeconds', ...
    'maximumPollSamples','lowPassCutoffHz','medianWindowSamples', ...
    'outlierMadThreshold','accelerationStartThreshold','accelerationStopThreshold', ...
    'lateralStartThreshold','lateralStopThreshold', ...
    'yawRateStartThresholdDegPerSecond','yawRateStopThresholdDegPerSecond', ...
    'verticalShockThreshold','jerkCandidateThreshold','minimumEventDurationSeconds', ...
    'maximumEventSilenceSeconds','verticalShockReleaseSamples','maximumSampleAgeMs', ...
    'historySeconds','eventHistoryLimit','maximumConsecutiveErrors','plotRefreshHz', ...
    'stopDrainTimeoutSeconds','stopDrainEmptyPasses','stopDrainPollIntervalSeconds', ...
    'maximumRecordingDurationSeconds','minimumFreeDiskBytes','maximumSessionBytes', ...
    'recordingGuardPeriodSeconds'};
for index = 1:numel(positive)
    validateattributes(config.(positive{index}), {'numeric'}, ...
        {'scalar','real','finite','positive'}, mfilename, positive{index});
end
integers = {'maximumPollSamples','medianWindowSamples','verticalShockReleaseSamples', ...
    'eventHistoryLimit','maximumConsecutiveErrors','stopDrainEmptyPasses'};
for index = 1:numel(integers)
    validateattributes(config.(integers{index}), {'numeric'}, {'integer'});
end
if config.stopDrainPollIntervalSeconds > config.stopDrainTimeoutSeconds
    error('IMU:InvalidRealtimeConfig', ...
        'stopDrainPollIntervalSeconds must not exceed stopDrainTimeoutSeconds.');
end
if mod(config.medianWindowSamples, 2) ~= 1
    error('IMU:InvalidRealtimeConfig', 'medianWindowSamples must be odd.');
end
if config.lowPassCutoffHz >= config.sampleRateHz / 2
    error('IMU:InvalidRealtimeConfig', 'lowPassCutoffHz must be below Nyquist.');
end
if ~(config.brakingStartThreshold < config.brakingStopThreshold && ...
        config.brakingStopThreshold < 0)
    error('IMU:InvalidRealtimeConfig', 'Braking hysteresis is invalid.');
end
if config.accelerationStopThreshold >= config.accelerationStartThreshold || ...
        config.lateralStopThreshold >= config.lateralStartThreshold || ...
        config.yawRateStopThresholdDegPerSecond >= ...
        config.yawRateStartThresholdDegPerSecond
    error('IMU:InvalidRealtimeConfig', 'Positive hysteresis is invalid.');
end
logicalFields = {'stopOnOverflow','stopOnSequenceGap','enableLivePlot', ...
    'enableRecording','UseTimer','DisconnectImuOnDelete','AllowSyntheticCalibration'};
for index = 1:numel(logicalFields)
    value = config.(logicalFields{index});
    if ~(islogical(value) && isscalar(value))
        error('IMU:InvalidRealtimeConfig', '%s must be logical.', logicalFields{index});
    end
end
if ~(ischar(config.recordingDirectory) || ...
        (isstring(config.recordingDirectory) && isscalar(config.recordingDirectory)))
    error('IMU:InvalidRealtimeConfig', 'recordingDirectory must be scalar text.');
end
end

