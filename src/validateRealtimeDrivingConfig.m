analysis = getDrivingAnalysisConfig(); imu = getImuConfig();
defaults = struct('sampleRateHz', imu.sampleRateHz, ...
    'callbackPeriodMs', imu.callbackPeriodMs, 'pollPeriodSeconds', 0.02, ...
    'maximumPollSamples', imu.callbackBufferCapacity, ...
    'lowPassCutoffHz', analysis.lowPassCutoffHz, ...
    'medianWindowSamples', analysis.medianWindowSamples, ...
    'outlierMadThreshold', analysis.outlierMadThreshold, ...
    'brakingStartThreshold', analysis.brakingStartThreshold, ...
    'brakingStopThreshold', analysis.brakingStopThreshold, ...
    'accelerationStartThreshold', analysis.accelerationStartThreshold, ...
    'accelerationStopThreshold', analysis.accelerationStopThreshold, ...
    'lateralStartThreshold', analysis.lateralStartThreshold, ...
    'lateralStopThreshold', analysis.lateralStopThreshold, ...
    'yawRateStartThresholdDegPerSecond', analysis.yawRateStartThresholdDegPerSecond, ...
    'yawRateStopThresholdDegPerSecond', analysis.yawRateStopThresholdDegPerSecond, ...
    'verticalShockThreshold', analysis.verticalShockThreshold, ...
    'jerkCandidateThreshold', analysis.jerkCandidateThreshold, ...
    'minimumEventDurationSeconds', analysis.minimumEventDurationSeconds, ...
    'maximumEventSilenceSeconds', 0.4, 'verticalShockReleaseSamples', 5, ...
    'maximumSampleAgeMs', imu.maximumCallbackSampleAgeMs, 'historySeconds', 30, ...
    'eventHistoryLimit', 1000, 'stopOnOverflow', true, ...
    'stopOnSequenceGap', false, 'maximumConsecutiveErrors', 3, ...
    'stopDrainTimeoutSeconds', 0.5, 'stopDrainEmptyPasses', 3, ...
    'stopDrainPollIntervalSeconds', 0.005, ...
    'enableLivePlot', true, 'plotRefreshHz', 10, 'enableRecording', false, ...
    'recordingDirectory', "sessions", ...
